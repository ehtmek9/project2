$('#cody1').on("click",pick1);
					function pick1(){
						const colorThief = new ColorThief();
						const color = colorThief.getColor($('.galary1')[0]);
						document.querySelector('#c1').style.backgroundColor = 'rgb(' + color + ')';
						var colors = colorThief.getPalette($('.galary1')[0],1);
						for(var i=0; i<colors.length; i++){
								$('#c1').after( $('<div class="c2"></div>').css('background-color','rgb('+ colors[i] +')'));
						}//for
				}//pick()

